#ifndef BACKLIGHT_TARGET_H
#define BACKLIGHT_TARGET_H

#include "tcc77x.h"

static inline bool _backlight_init(void)
{
    GPIOA_DIR |= 0x40;
    return true;
}

static inline void _backlight_on(void)
{
    /* Enable backlight */
    GPIOA |= 0x40;
}

static inline void _backlight_off(void)
{
    /* Disable backlight */
    GPIOA &= ~0x40;
}

#endif
