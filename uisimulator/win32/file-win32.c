#include <windows.h>
#include "file-win32.h"
#include "file.h"

// Directory operations
//

// opendir
// open directory for scanning
DIR *opendir (
              char *dirname // directory name
              )
{
    DIR *p = (DIR*)malloc(sizeof(DIR));
    if (_findfirst (dirname, &p) == -1)
        return NULL;
    return p;
}